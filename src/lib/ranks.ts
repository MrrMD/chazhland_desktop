/**
 * Цвет ранга по уровню (тир-бэнды), совпадает с тематическими тирами: серый → бронза → серебро →
 * золото → тил → сине-фиолет → акцент → фиолет. Mid-тона читаемы и на светлой, и на тёмной теме
 * (как hex-цвета кастомных ролей). Используется для тинта ранг-чипа.
 */
export function rankColor(level: number): string {
  if (level >= 201) return '#7c5cff' // Запределье
  if (level >= 171) return '#e0457b' // Светило / Эпоха / Вершина
  if (level >= 137) return '#5b6cff' // Легенда / Титан / Мифический
  if (level >= 101) return '#13b886' // Хранитель / Старейшина
  if (level >= 65) return '#e7c14b'  // Знаток / Мастер
  if (level >= 35) return '#9aa5b1'  // Старожил / Ветеран
  if (level >= 13) return '#cd7f32'  // Свой человек / Завсегдатай
  return '#9b958b'                   // Новичок / Прохожий
}
